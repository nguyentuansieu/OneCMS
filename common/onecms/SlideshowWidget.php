<?php

namespace common\onecms;

use common\models\Advertising;
use yii\base\Widget;

class SlideshowWidget extends Widget
{

    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub
    }

    public function run()
    {
        parent::run(); // TODO: Change the autogenerated stub
        $advertingModel = new Advertising();
        $nodes = $advertingModel->find()->where(['position' => 'slideshow'])->all();
        return $this->render('SlideshowWidget', [
            'nodes' => $nodes,
        ]);
    }
}